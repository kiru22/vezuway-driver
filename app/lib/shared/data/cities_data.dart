  static List<CityModel> getCitiesByCountries(List<String> countries) {
    return allCities.where((city) => countries.contains(city.country)).toList();
  }

  static List<CityModel> searchCities(
    String query, {
    List<String>? countries,
    List<CityModel>? excludeCities,
    String? locale,
  }) {
    final normalizedQuery = query.toLowerCase().trim();
    var cities = countries != null ? getCitiesByCountries(countries) : allCities;

    if (excludeCities != null && excludeCities.isNotEmpty) {
      cities = cities.where((city) => !excludeCities.contains(city)).toList();
    }

    if (normalizedQuery.isEmpty) {
      return cities;
    }

    return cities.where((city) {
      // Search in base name
      if (city.name.toLowerCase().contains(normalizedQuery)) return true;
      // Search in Spanish name
      if (city.nameEs.toLowerCase().contains(normalizedQuery)) return true;
      // Search in Ukrainian name
      if (city.nameUk.toLowerCase().contains(normalizedQuery)) return true;
      return false;
    }).toList();
  }
